package miridih.objects;

public class Shape {
    private double startX;
    private double startY;
    private double endX;
    private double endY;

    public void setStart(double x, double y) {
        startX = x;
        startY = y;
    }

    public void setEnd(double x, double y) {
        endX = x;
        endY = y;
    }
}
